package project;

public class UserCredentials {
	private String userName;
	private char[] pwd;
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public char[] getPwd() {
		return pwd;
	}
	public void setPwd(char[] pass) {
		this.pwd = pass;
	}
	
	
}
